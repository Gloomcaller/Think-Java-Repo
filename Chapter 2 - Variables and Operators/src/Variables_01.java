public class Variables_01 {

	public static void main(String[] args) {
		// byte: 8-bit signed integer, range: -128 to 127
		byte byteVar = 1;

		// short: 16-bit signed integer, range: -32,768 to 32,767
		short shortVar = 01;

		// int: 32-bit signed integer, range: -2^31 to 2^31-1
		int intVar = 1234;

		// long: 64-bit signed integer, range: -2^63 to 2^63-1
		long longVar = 12345678L;

		// float: 32-bit floating point, range: approximately. ±1.4e-45 to ±3.4e38w
		float floatVar = 1234f;

		// double: 64-bit floating point, range: approximately. ±4.9e-324 to ±1.8e308
		double doubleVar = 12345678.12345678;

		// char: 16-bit Unicode character, range: '\u0000' (0) to '\uffff' (65,535)
		char charVar = 'a';

		// String: a sequence of characters
		String stringVar = "abc";
	}
}
