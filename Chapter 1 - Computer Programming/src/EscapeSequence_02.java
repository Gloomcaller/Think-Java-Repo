
public class EscapeSequence_02 {

	public static void main(String[] args) {

		System.out.println("This is the first line.\nThis is the second line.");

		System.out.println("Column1\tColumn2\tColumn3");
		System.out.println("Row1\tData1\tData2");

		System.out.println("He said, \"Java is fun!\"");

		System.out.println("This is a backslash: \\");

		System.out.println("Item\tDescription\n1\t\"Laptop\"\tA portable computer\n2\t\"Phone\"\tA mobile device\n");
		System.out.println("Path to project folder: C:\\\\Users\\\\Username\\\\Projects\\\\EscapeSequence");

	}

}
