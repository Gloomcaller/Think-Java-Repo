
public class HelloWorld_01 {

	public static void main(String[] args) {
		// Beginning of the story
		System.out.println("One sunny afternoon, I was walking through the park when I heard a voice behind me:");
		System.out.println("Stranger: Hello, World!");

		// Turning around to see who it was
		System.out.println("I turned around and saw a friendly looking stranger. She smiled warmly and said,");
		System.out.println("Stranger: Hello there!.");

		// Responding to the greeting
		System.out.println("Me: Hello to you too.");

		// Continuing the conversation
		System.out.println("Stranger: How are you?");
		System.out.println("Me: I'm good, thank you. How about you?");

		// Stranger's response
		System.out.println("Stranger: I'm doing well, thanks for asking!");

		// Further small talk
		System.out.println("Me: It's a beautiful day, isn't it?");
		System.out.println("Stranger: Absolutely! Perfect weather for a walk.");

		// Introducing ourselves
		System.out.println("Me: By the way, I'm Alex.");
		System.out.println("Stranger: Nice to meet you, Alex. I'm Jan.");

		// Continuing to chat
		System.out.println("Me: So, Jan, what brings you to the park today?");
		System.out.println("Jan: Just enjoying the weather and getting some fresh air. How about you?");
		System.out.println("Me: Same here. It's great to get out and enjoy nature.");

		// Wrapping up the conversation
		System.out.println("Jan: It was nice chatting with you, Alex. Have a great day!");
		System.out.println("Me: You too, Jan. Goodbye!");

		// Ending the story
		System.out.println("As I continued my walk, I couldn't help but smile at the pleasant encounter.");
	}

}
